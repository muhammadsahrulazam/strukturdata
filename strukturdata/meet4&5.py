# belajar aritmatika dasar
x = 11
y = 4

# penjumlahan +
hasil = x + y
print ("x + y =",hasil)

# Pengurangan -
hasil = x - y
print("x - y =", hasil)

# Perkalian *
hasil = x * y
print("x * y =", hasil)

# Pembagian /
hasil = x / y
print("x / y =", hasil)

# Perpangkatan **
hasil = x ** y
print("x ^ y =", hasil)

# Modulo %
hasil = y % x
print("x % y =", hasil)

# Floor Division //
hasil = x // y
print("x // y =", hasil)

